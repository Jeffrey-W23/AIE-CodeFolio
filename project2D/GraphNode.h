// #include, using, etc
#pragma once
#include "LinkedLists.h"

//--------------------------------------------------------------------------------------
// GraphNode object.
//--------------------------------------------------------------------------------------
template <typename T>
class GraphNode
{
public:

	//--------------------------------------------------------------------------------------
	// Default Constructor.
	//--------------------------------------------------------------------------------------
	GraphNode() 
	{
		m_bTraversed = false;
	}

	//--------------------------------------------------------------------------------------
	// Constructor taking in type T data.
	//--------------------------------------------------------------------------------------
	GraphNode(T data)
	{
		m_Data = data;
		m_bTraversed = false;
	}

	//--------------------------------------------------------------------------------------
	// Default Destructor
	//--------------------------------------------------------------------------------------
	~GraphNode() {}

	//--------------------------------------------------------------------------------------
	// AddConnection: Add a connection to the graph.
	//
	// Param:
	//		other: a GraphNode pointer taking in T. What to add to the graph.
	//--------------------------------------------------------------------------------------
	void AddConnection(GraphNode<T>* other)
	{
		m_Edges.PushBack(other);
	}

	//--------------------------------------------------------------------------------------
	// RemoveConnection: Remove a connection from the graph.
	//
	// Param:
	//		other: a GraphNode pointer taking in T. What you want to remove from the graph
	//--------------------------------------------------------------------------------------
	void RemoveConnection(GraphNode<T>* other)
	{
		m_Edges.Remove(other);
	}

	//--------------------------------------------------------------------------------------
	// A Linked list of GraphNodes pointers taking in T.
	//--------------------------------------------------------------------------------------
	LinkedLists<GraphNode<T>*> m_Edges;
	
	//--------------------------------------------------------------------------------------
	// A Type T m_Data.
	//--------------------------------------------------------------------------------------
	T m_Data;
	
	//--------------------------------------------------------------------------------------
	// A bool m_bTraversed.
	//--------------------------------------------------------------------------------------
	bool m_bTraversed;
};