// #includes, using, etc
#pragma once
#include "DynamicArray.h"

// forward declarations
struct  AStarNode;

//--------------------------------------------------------------------------------------
// AStarEdge object.
//--------------------------------------------------------------------------------------
struct AStarEdge
{
	AStarNode* m_nEndNode;
	int m_nCost;
};

//--------------------------------------------------------------------------------------
// AStarNode object.
//--------------------------------------------------------------------------------------
struct AStarNode
{
	//--------------------------------------------------------------------------------------
	// Default Constructor.
	//--------------------------------------------------------------------------------------
	AStarNode(int nIndex)
	{
		// Set variables
		m_nPrev = nullptr;
		m_nGScore = 0;
		m_nHScore = 0;
		m_nFScore = 0;
		m_nIndex = nIndex;
		m_Blocked = false;
	}

	//--------------------------------------------------------------------------------------
	// Virtual Default Destructor
	//--------------------------------------------------------------------------------------
	virtual ~AStarNode()
	{
		for (int i = 0; i < m_AdjacentList.Size(); ++i)
		{
			delete m_AdjacentList[i];
		}
	}

	//--------------------------------------------------------------------------------------
	// A AStar pointer m_pAstar.
	//--------------------------------------------------------------------------------------
	AStarNode* m_nPrev;

	//--------------------------------------------------------------------------------------
	// An int for the g score.
	//--------------------------------------------------------------------------------------
	int m_nGScore;

	//--------------------------------------------------------------------------------------
	// An int for the h score.
	//--------------------------------------------------------------------------------------
	int m_nHScore;

	//--------------------------------------------------------------------------------------
	// An int for the f score.
	//--------------------------------------------------------------------------------------
	int m_nFScore;

	//--------------------------------------------------------------------------------------
	// A int Index
	//--------------------------------------------------------------------------------------
	int m_nIndex;

	//--------------------------------------------------------------------------------------
	// A bool for blocking a node.
	//--------------------------------------------------------------------------------------
	bool m_Blocked;

	//--------------------------------------------------------------------------------------
	// A DynamicArray of AstarEdge pointers.
	//--------------------------------------------------------------------------------------
	DynamicArray<AStarEdge*> m_AdjacentList;
};